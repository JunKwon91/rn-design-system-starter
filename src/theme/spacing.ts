// ============================================================================
// LottoStats 간격(spacing) & 모서리 반경(radius) 토큰
// ============================================================================
//
// [개념: 4px 베이스 그리드 (4px base unit)]
// 모든 간격은 4의 배수만 사용한다(4, 8, 12, 16, 20, 24, ...).
// 왜?
//   1) 시각적 일관성 — 16px 옆에 17px이 섞이면 미묘하게 어긋나 보임
//   2) 디자이너-개발자 합의 — 디자인 도구도 4px 그리드 기준이면 코드와 1:1 동기화 가능
//   3) 디자인 시스템의 표준 (Material Design, iOS HIG, Tailwind 모두 4px 또는 8px 기반)
//
// [개념: 토큰 이름 규칙]
// 숫자 대신 의미 있는 이름(xs/sm/md/lg/xl)을 사용.
// 컴포넌트가 spacing[16]을 쓰면 "16이 뭘 의미하지?"가 되지만,
// spacing.lg를 쓰면 "큰 간격"이라는 의미가 즉시 전달된다.
//
// [개념: as const]
// TypeScript에 "이 값들은 변하지 않는 readonly이고, 각 숫자는 정확한
// 리터럴 타입(예: 16 자체)으로 좁혀라"라고 지시.
// 실수로 spacing.md를 다른 값으로 덮어쓰는 것을 컴파일러가 막아준다.
// ============================================================================

// ----------------------------------------------------------------------------
// spacing — 패딩, 마진, gap 등의 간격 값 (단위: px)
// ----------------------------------------------------------------------------
// 사용 예) padding: theme.spacing.md → 12px
//          gap: theme.spacing.lg → 16px
// ----------------------------------------------------------------------------
export const spacing = {
  // 기본 t-shirt 사이즈 스케일 (xs ~ 4xl)
  // 작은 단위는 4px씩, 큰 단위는 더 큰 폭으로 증가 (시각적 점프 자연스럽게)
  none: 0, // 간격 없음
  xs: 4, // 매우 작음 — 아이콘 옆 텍스트 사이
  sm: 8, // 작음 — 라벨과 인풋 사이
  md: 12, // 중간 — 카드 내부 행 사이
  lg: 16, // 큼 — 표준 카드 padding, 화면 좌우 여백
  xl: 24, // 더 큼 — 큰 섹션 사이
  '2xl': 32, // 매우 큰 섹션 사이
  '3xl': 48, // 페이지 상단 여백 등
  '4xl': 64, // 가장 큰 단위

  // 의미 있는 토큰 — 위 스케일과 같은 값이지만 용도를 이름에 명시
  // 컴포넌트 코드 가독성↑, 나중에 정책 변경 시 한 곳만 수정
  containerMargin: 16, // 화면 좌우 외곽 여백 (=lg와 동일)
  gutter: 12, // 그리드 사이 간격 (=md와 동일)
  stackSm: 8, // 관련 요소 사이 (라벨↔인풋)
  stackMd: 16, // 표준 컴포넌트 사이
  stackLg: 24, // 큰 논리 섹션 사이
} as const;

// ----------------------------------------------------------------------------
// radius — 모서리 반경 (border-radius) 토큰 (단위: px)
// ----------------------------------------------------------------------------
// 사용 예) borderRadius: theme.radius.lg → 16px (카드)
//          borderRadius: theme.radius.full → 9999 (완전한 원/캡슐)
// ----------------------------------------------------------------------------
export const radius = {
  none: 0, // 직각 모서리
  sm: 4, // 매우 작은 둥글기 (체크박스 등)
  base: 8, // 기본 둥글기 (인풋, 작은 버튼)
  md: 12, // 중간 (중간 카드)
  lg: 16, // 큰 둥글기 (메인 카드)
  xl: 24, // 매우 큰 (Bottom Nav, Segmented Control 등 캡슐형 컨테이너)
  full: 9999, // 완전한 원/타원 (로또 공, 동그란 아바타)
} as const;

// ----------------------------------------------------------------------------
// 타입 추출 — 다른 파일에서 spacing/radius 값의 타입을 참조할 때 사용
// ----------------------------------------------------------------------------
// [개념: typeof]
// `typeof spacing`은 "spacing이라는 값의 타입"을 의미.
// `as const`로 좁혀진 spacing의 타입을 그대로 가져온다.
// 결과: { readonly none: 0; readonly xs: 4; readonly sm: 8; ... }
//
// 이 Spacing/Radius 타입은 다른 파일이 "이 객체와 똑같은 모양의 객체"를
// 받고 싶을 때 사용한다.
// 예) function withPadding(p: keyof Spacing) { ... }
//     → 'xs' | 'sm' | 'md' | 'lg' | ... 만 받도록 제한
export type Spacing = typeof spacing;
export type Radius = typeof radius;
